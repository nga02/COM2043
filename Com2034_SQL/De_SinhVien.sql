IF OBJECT_ID('sp_Xoa') IS NOT NULL 
DROP PROC   sp_Xoa
GO 
CREATE PROC sp_Xoa @Diem INT
AS
BEGIN
	BEGIN TRY
		BEGIN TRAN
			DECLARE @Table TABLE(MaSV VARCHAR(10))
			INSERT INTO @Table 
			SELECT SINHVIEN.MaSV FROM SINHVIEN JOIN DIEM ON DIEM.MaSV=SINHVIEN.MaSV
			WHERE ((DiemLan1+DiemLan2)/2) > @Diem

			DELETE FROM DIEM WHERE MaSV IN (SELECT MaSV FROM @Table)
			DELETE FROM SINHVIEN WHERE MaSV IN (SELECT MaSV FROM @Table)
		COMMIT TRAN
	END TRY

	BEGIN CATCH
		ROLLBACK TRAN
	END CATCH 
END


