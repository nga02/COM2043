DROP FUNCTION   f_SINHVIEN
GO 
CREATE FUNCTION f_SINHVIEN 
(   @HoTen NVARCHAR(50),
	@NgaySinh DATE,
	@GioiTinh VARCHAR(5),
	@Lop INT
)
RETURNS NVARCHAR(100)
AS
BEGIN
	IF NOT EXISTS (SELECT * FROM SINHVIEN WHERE @HoTen=HoTen AND @NgaySinh=NgaySinh AND @GioiTinh= GioiTinh AND @Lop=Lop)
		RETURN N'KHÔNG TỒN TẠI SINH VIÊN ĐÓ'
		RETURN N'MÃ SINH VIEN LÀ:' + CAST((SELECT MaSV FROM SINHVIEN 
		WHERE @HoTen=HoTen AND @NgaySinh=NgaySinh AND @GioiTinh= GioiTinh AND @Lop=Lop) 
		AS VARCHAR(10))
END

