IF OBJECT_ID('vi_Top2') IS NOT NULL 
DROP VIEW   vi_Top2
GO 
CREATE VIEW vi_Top2 
AS
	SELECT TOP 2 SINHVIEN.MaSV, HoTen,TenMonHoc, DiemLan1, DiemLan2, SUM(DiemLan1 + DiemLan2) AS N'Tổng điểm'
	FROM SINHVIEN JOIN DIEM ON DIEM.MaSV=SINHVIEN.MaSV 
				  JOIN MONHOC ON DIEM.MaMonHoc=MONHOC.MaMonHoc
	GROUP BY SINHVIEN.MaSV, HoTen,TenMonHoc, DiemLan1, DiemLan2
	ORDER BY SUM(DiemLan1 + DiemLan2)  DESC

	SELECT * FROM vi_Top2 

